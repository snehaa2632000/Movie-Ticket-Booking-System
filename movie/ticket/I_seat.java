    private void jToggleButton_E13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton_E13ActionPerformed
        // TODO add your handling code here:
        if (jToggleButton_E13.isSelected()) {
            Count++;
            if (Count <= ticket_count) {
                jToggleButton_E13.setBackground(Color.ORANGE);
                seats.add(jToggleButton_E13.getName());
                jLabel1.setText(Arrays.toString(seats.toArray()).replace("[", "").replace("]", ""));
            } else {
                JOptionPane.showMessageDialog(this, "You are Selecting more than " + ticket_count + " tickets.");
                jToggleButton_E13.setSelected(false);
                Count--;
            }
        } else {
            Count--;
            seats.remove(jToggleButton_E13.getName());
            jLabel1.setText(Arrays.toString(seats.toArray()).replace("[", "").replace("]", ""));
            jToggleButton_E13.setBackground(new java.awt.Color(0, 153, 0));
        }
    }//GEN-LAST:event_jToggleButton_E13ActionPerformed

